def main():
    with open('kotest.json', encoding='utf-8') as f:
        data = json.load(f)
    assert len(data) == len(JP_EXPLAIN), f'{len(data)} items vs {len(JP_EXPLAIN)} translations'
    for item, (jp, explain) in zip(data, JP_EXPLAIN):
        item['jp'] = jp
        item['explain'] = explain
    with open('kotest.json', 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=1)
    print(f'updated {len(data)} items with jp + explain')


if __name__ == '__main__':
    main()
